"""
Benchmark Runner

Purpose: Run benchmarks on JSONL datasets with any model and scoring function.
"""

import json
import re
from pathlib import Path
from typing import Callable, Dict, Any, List, Tuple, Optional
from tqdm import tqdm
from math import comb
from abyme.model import Model
from abc import ABC, abstractmethod

class Benchmark(ABC):
    """
    Abstract base class for benchmarks. Defines the interface for running benchmarks.
    """
    @property
    @abstractmethod
    def name(self):
        """The name of the benchmark, used for file naming and organization."""
        pass


    @abstractmethod
    def download(self):
        """
        Download or prepare the dataset for benchmarking.
        Save the dataset to "data/{self.name}.jsonl" for use in generation and scoring.
        """
        pass
    
    @abstractmethod
    def generate(self, model: Model, input: Dict[str, Any]) -> str:
        """
        Generate model outputs for the benchmark using the provided model.

        Args:
            model: An instance of a Model to generate responses
            input: A json of the input data for a single problem
        Returns:
            A string output generated by the model for the given input
        """        
        pass
    
    def generate_all(self, model: Model, test_name: str):
        """
        Generate model outputs for the entire benchmark dataset.
        Save the output to "results/{self.name}/{test_name}.jsonl" with each line containing the original input data plus a "model_output" field.

        Args:
            model: An instance of a Model to generate responses
            test_name: A name for this generation run (used for output file naming)
        """
        # Set up paths
        input_path = Path(f"data/{self.name}.jsonl")
        output_path = Path(f"results/{self.name}/{test_name}.jsonl")

        # Check if input file exists
        if not input_path.exists():
            raise FileNotFoundError(f"Dataset file does not exist: {input_path}")

        # Create output directory if it doesn't exist
        output_path.parent.mkdir(parents=True, exist_ok=True)

        # Count total lines for progress bar
        with input_path.open('r') as f:
            total_lines = sum(1 for line in f if line.strip())

        # Open output file for writing
        with output_path.open('w') as outfile:
            # Read input file line by line with progress bar
            with input_path.open('r') as infile:
                for line in tqdm(infile, total=total_lines, desc=f"Generate: {test_name}"):
                    # Skip empty lines
                    if not line.strip():
                        continue

                    # Parse JSON
                    json_data = json.loads(line.strip())

                    # Generate output using the abstract generate method
                    output = self.generate(model, json_data)

                    # Prepare output record
                    output_record = {
                        **json_data,  # Include all original fields
                        'output': output
                    }

                    # Write to output file immediately
                    outfile.write(json.dumps(output_record) + '\n')
                    outfile.flush()  # Ensure it's written immediately

    @abstractmethod
    def score(self, model_output: str, input: Dict[str, Any]) -> float:
        """
        Score a single model output against the input data.

        Args:
            model_output: The output string generated by the model
            input: The original input data for the problem (as a dict)
        Returns:
            A float score representing the quality of the model output
        """
        pass
    
    def score_all(self, test_name: str):
        """
        Score all model outputs for a given test run.
        Read from "results/{self.name}/{test_name}.jsonl" and write scores to "results/{self.name}/{test_name}_scored.jsonl" with an added "score" field.

        Args:
            test_name: The name of the generation run to score (used for input/output file naming)
        """
        # Set up paths
        input_path = Path(f"results/{self.name}/{test_name}.jsonl")
        output_path = Path(f"results/{self.name}/{test_name}_scored.jsonl")

        # Check if input file exists
        if not input_path.exists():
            raise FileNotFoundError(f"Generated results file does not exist: {input_path}")

        # Create output directory if it doesn't exist (should already exist, but just in case)
        output_path.parent.mkdir(parents=True, exist_ok=True)

        # Count total lines for progress bar
        with input_path.open('r') as f:
            total_lines = sum(1 for line in f if line.strip())

        # Open output file for writing
        with output_path.open('w') as outfile:
            # Read input file line by line with progress bar
            with input_path.open('r') as infile:
                for line in tqdm(infile, total=total_lines, desc=f"Score: {test_name}"):
                    # Skip empty lines
                    if not line.strip():
                        continue

                    # Parse JSON
                    json_data = json.loads(line.strip())

                    # Ensure output field exists
                    if 'output' not in json_data:
                        raise ValueError(f"Missing 'output' field in {input_path}")

                    # Score the output using the abstract score method
                    score = self.score(json_data['output'], json_data)

                    # Prepare output record
                    output_record = {
                        **json_data,  # Include all original fields
                        'score': score
                    }

                    # Write to output file immediately
                    outfile.write(json.dumps(output_record) + '\n')
                    outfile.flush()  # Ensure it's written immediately  
    
    def check_scores(self, test_name: str) -> Tuple[float, List[float]]:
        """
        Check the scored results and calculate average score.
        print the average score

        Args:
            test_name: The name of the generation run to check scores for (used for input file naming)

        Returns:
            Tuple of (average_score, score_list)
        """
        result_path = Path(f"results/{self.name}/{test_name}_scored.jsonl")

        if not result_path.exists():
            raise FileNotFoundError(f"Scored results file does not exist: {result_path}")

        scores = []

        with result_path.open('r') as f:
            for line in f:
                if not line.strip():
                    continue

                json_data = json.loads(line.strip())
                if 'score' in json_data:
                    scores.append(json_data['score'])

        if not scores:
            raise ValueError(f"No valid scores found in {result_path}")

        average_score = sum(scores) / len(scores)
        print(f"Average score for {test_name}: {average_score:.4f}")
        return average_score, scores


def generate_n_samples(n: int) -> Callable[[Model, Dict], str]:
    """
    Factory function that returns a generation function that generates n samples.

    Args:
        n: Number of samples to generate

    Returns:
        A generation function with signature (Model, Dict) -> str
        The output is a concatenated string of all n samples.

    Example:
        generator = generate_n_samples(n=10)
        outputs = generator(model, json_data)
    """
    def generation_function(model: Model, json_data: Dict) -> str:
        """
        Generate n solutions.

        Args:
            model: Model instance to generate responses
            json_data: Dictionary containing problem data

        Returns:
            Concatenated string of all n samples
        """
        problem = json_data['problem']
        outputs = []

        for i in range(n):
            # Generate output with error handling
            try:
                output = model.generate(problem, max_attempt=1)
                outputs.append(f"=== Sample {i+1}/{n} ===\n{output}")
            except Exception as e:
                # If model throws an error, record it
                error_message = f"ERROR: {str(e)}"
                outputs.append(f"=== Sample {i+1}/{n} ===\n{error_message}")

        # Concatenate all outputs
        return "\n\n".join(outputs)

    return generation_function


def score_pass_at_k(
    n: int,
    k: int,
    decider_function: Callable[[str, Dict], bool]
) -> Callable[[str, Dict], float]:
    """
    Factory function that returns a scoring function for pass@k evaluation.

    This function creates a scorer that parses n samples and calculates the probability
    that at least 1 of k randomly selected samples is correct using the formula:
    pass@k = 1 - C(n-c, k) / C(n, k)
    where n is total samples, c is correct samples, k is samples evaluated.

    Args:
        n: Total number of samples (must match generation)
        k: Number of samples to evaluate (k <= n)
        decider_function: Function with signature (sample_output: str, json_data: dict) -> bool
                         Returns True if a single sample output is correct, False otherwise

    Returns:
        A scoring function with signature (model_output: str, Dict) -> float

    Example:
        def my_decider(output: str, data: dict) -> bool:
            return extract_answer(output) == data['ground_truth']

        scorer = score_pass_at_k(n=10, k=5, decider_function=my_decider)
        score = scorer(concatenated_output, json_data)
    """
    if k > n:
        raise ValueError(f"k ({k}) must be <= n ({n})")

    def scoring_function(model_output: str, json_data: Dict) -> float:
        """
        Parse n samples from concatenated output and calculate pass@k score.

        Args:
            model_output: Concatenated string of all n samples
            json_data: Dictionary containing problem data

        Returns:
            pass@k score (probability that at least 1 of k samples is correct)
        """
        # Parse samples from concatenated output
        # Expected format: "=== Sample X/n ===\n{output}\n\n=== Sample Y/n ===\n{output}..."
        samples = []
        current_sample = []

        for line in model_output.split('\n'):
            if line.startswith('=== Sample '):
                # Start of a new sample
                if current_sample:
                    samples.append('\n'.join(current_sample))
                current_sample = []
            else:
                current_sample.append(line)

        # Add the last sample
        if current_sample:
            samples.append('\n'.join(current_sample))

        # Count correct samples
        c = 0
        for sample in samples:
            try:
                if decider_function(sample.strip(), json_data):
                    c += 1
            except Exception:
                # If decider throws an error, count as wrong
                pass

        # Calculate pass@k score using the formula: pass@k = 1 - C(n-c, k) / C(n, k)
        if n - c < k:
            score = 1.0
        else:
            # Formula: 1 - (comb(n-c, k) / comb(n, k))
            negative_space = comb(n - c, k)
            total_space = comb(n, k)
            score = 1.0 - (negative_space / total_space)

        return float(score)

    return scoring_function


def extract_boxed_answer(text: str) -> Optional[str]:
    """
    Extract the answer from LaTeX \\boxed{...} notation.

    Searches from the end of the string backwards to get the final answer,
    as Chain-of-Thought models might hallucinate boxed numbers in intermediate steps.
    Uses brace-counting to correctly handle nested LaTeX braces.

    Args:
        text: The raw model output

    Returns:
        The extracted content as a string, or None if not found

    Example:
        >>> extract_boxed_answer("The answer is \\boxed{42}")
        '42'
        >>> extract_boxed_answer("\\boxed{\\frac{1}{2}}")
        '\\frac{1}{2}'
    """
    if not text:
        return None

    # Pattern to match \boxed{...} - find all occurrences
    pattern = r'\\boxed\{([^}]*)\}'
    matches = list(re.finditer(pattern, text))

    if not matches:
        return None

    # Take the last match (searching from the end backwards)
    last_match = matches[-1]
    content = last_match.group(1)

    # Handle nested braces if necessary
    if '{' in content or '}' in content:
        # Count braces to extract the full content
        start_pos = last_match.start()
        brace_count = 0
        result_chars = []
        i = start_pos + len('\\boxed{')

        while i < len(text):
            char = text[i]
            if char == '{':
                brace_count += 1
                result_chars.append(char)
            elif char == '}':
                if brace_count == 0:
                    break
                brace_count -= 1
                result_chars.append(char)
            else:
                result_chars.append(char)
            i += 1

        content = ''.join(result_chars)

    return content.strip()




